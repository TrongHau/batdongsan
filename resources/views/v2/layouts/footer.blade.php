                return addErrorInput(name, 'Tên hiển thị không được để trống');
            }
            if(!email.val()) {
                return addErrorInput(email, 'Địa chỉ E-Mail không được để trống');
            }
            if(!password.val()) {
                return addErrorInput(password, 'Mật khẩu không được để trống.');
            }
            if(confirm_password.val() != password.val()) {
                return addErrorInput(confirm_password, 'Xác nhận mật khẩu không khớp.');
            }
            if(email.val().length < 4) {
                return addErrorInput(email, 'Địa chỉ E-Mail trên 4 ký tự.');
            }
            if(password.val().length < 6) {
                return addErrorInput(password, 'Mật khẩu phải trên 6 ký tự.');
            }
            $.ajax({
                url: window.location.origin + '/register',
                type: "POST",
                dataType: "json",
                data: {
                    name: name.val(),
                    email: email.val(),
                    password: password.val(),
                    password_confirmation: confirm_password.val(),

                },
                beforeSend: function () {
                    if(loaded) return false;
                    loaded = true;
                },
                error: function (data) {
                    var errors = data.responseJSON;
                    $.each( errors.errors, function( key, value ) {
                        addErrorInput($('.register-form').find('.' + key), value);
                    });
                },
                success: function(response) {
                    window.location = '/thong-tin-ca-nhan';
                }
            });
        }
